package com.toast.management.dto;

public class CompInfo {
	private String comp_name;
	private int ceo_idx;
	private String comp_stamp;
	private String comp_phone;
	private String comp_addr;
	private String comp_biz_no;
	private String comp_corp_no;
	private String comp_est_date;
	public String getComp_name() {
		return comp_name;
	}
	public void setComp_name(String comp_name) {
		this.comp_name = comp_name;
	}
	public int getCeo_idx() {
		return ceo_idx;
	}
	public void setCeo_idx(int ceo_idx) {
		this.ceo_idx = ceo_idx;
	}
	public String getComp_stamp() {
		return comp_stamp;
	}
	public void setComp_stamp(String comp_stamp) {
		this.comp_stamp = comp_stamp;
	}
	public String getComp_phone() {
		return comp_phone;
	}
	public void setComp_phone(String comp_phone) {
		this.comp_phone = comp_phone;
	}
	public String getComp_addr() {
		return comp_addr;
	}
	public void setComp_addr(String comp_addr) {
		this.comp_addr = comp_addr;
	}
	public String getComp_biz_no() {
		return comp_biz_no;
	}
	public void setComp_biz_no(String comp_biz_no) {
		this.comp_biz_no = comp_biz_no;
	}
	public String getComp_corp_no() {
		return comp_corp_no;
	}
	public void setComp_corp_no(String comp_corp_no) {
		this.comp_corp_no = comp_corp_no;
	}
	public String getComp_est_date() {
		return comp_est_date;
	}
	public void setComp_est_date(String comp_est_date) {
		this.comp_est_date = comp_est_date;
	}
	public String getFile_key() {
		return file_key;
	}
	public void setFile_key(String file_key) {
		this.file_key = file_key;
	}
	private String file_key;
}
